    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // getText
        String kodeObat = kodeInput.getText();
        String stokObat = stokInput.getText();
        String hargaObat = hargaInput.getText();
        String namaObat = namaInput.getText();

        // exception getJRadioButton
        String jenisObat = "";

        if (opsiSirup.isSelected()) {
            jenisObat = "Sirup";
        } else if (opsiCair.isSelected()) {
            jenisObat = "Cair";
        } else if (opsiKapsul.isSelected()) {
            jenisObat = "Kapsul";
        } else if (opsiTablet.isSelected()) {
            jenisObat = "Tablet";
        }

        // Validation
        if (kodeObat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kode Obat tidak boleh kosong.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (stokObat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Stok Obat tidak boleh kosong.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (hargaObat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harga Obat tidak boleh kosong.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (namaObat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Obat tidak boleh kosong.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (jenisObat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih jenis Obat.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Buat objek Obat baru
            Obat newObat = new Obat();
            newObat.setKode(kodeObat);
            newObat.setNama(namaObat);
            newObat.setHarga(hargaObat);
            newObat.setStok(stokObat);
            newObat.setJenis(jenisObat);

            // Add Obat to Table
            DefaultTableModel model = (DefaultTableModel) tableObat.getModel();
            model.addRow(new Object[]{newObat.getKode(), newObat.getNama(), newObat.getHarga(), newObat.getStok(), newObat.getJenis()});

            // Simpan data ke obatList
            obatList.add(newObat);

            // InputBufferClear
            kodeInput.setText("");
            namaInput.setText("");
            hargaInput.setText("");
            stokInput.setText("");
            jenisObatButtonGroup.clearSelection();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    
    private void radioGroup(){
        jenisObatButtonGroup = new ButtonGroup();
        jenisObatButtonGroup.add(opsiSirup);
        jenisObatButtonGroup.add(opsiCair);
        jenisObatButtonGroup.add(opsiKapsul);
        jenisObatButtonGroup.add(opsiTablet);
    }
            
    private void initTable() {
        model = (DefaultTableModel) tableObat.getModel();
        model.setRowCount(0);

        for (Obat obat : obatList) {
            model.addRow(new Object[]{
                obat.getKode(),
                obat.getNama(),
                obat.getHarga(),
                obat.getStok(),
                obat.getJenis()
            });
        }
    }
    
    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = tableObat.getSelectedRow();
        if (selectedRow >= 0) {
            // getTable
            DefaultTableModel model = (DefaultTableModel) tableObat.getModel();

            // getInput
            String kodeObat = kodeInput.getText();
            String stokObat = stokInput.getText();
            String hargaObat = hargaInput.getText();
            String namaObat = namaInput.getText();
            //bagaimana cara input jenisObat
            String jenisObat = "";

            if (opsiSirup.isSelected()) {
                jenisObat = "Sirup";
            } else if (opsiCair.isSelected()) {
                jenisObat = "Cair";
            } else if (opsiKapsul.isSelected()) {
                jenisObat = "Kapsul";
            } else if (opsiTablet.isSelected()) {
                jenisObat = "Tablet";
            }

            if (kodeObat.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama Awal harus diisi.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (stokObat.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama Akhir harus diisi.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (hargaObat.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama Akhir harus diisi.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (namaObat.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama Akhir harus diisi.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (jenisObat.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama Akhir harus diisi.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                model.setValueAt(kodeObat, selectedRow, 0);
                model.setValueAt(namaObat, selectedRow, 1);
                model.setValueAt(hargaObat, selectedRow, 2);
                model.setValueAt(stokObat, selectedRow, 3);
                model.setValueAt(jenisObat, selectedRow, 4);

                // Perbarui data dalam obatList
                Obat obatToUpdate = obatList.get(selectedRow);
                obatToUpdate.setKode(kodeObat);
                obatToUpdate.setNama(namaObat);
                obatToUpdate.setHarga(hargaObat);
                obatToUpdate.setStok(stokObat);
                obatToUpdate.setJenis(jenisObat);

                // Membersihkan inputNamaAwal dan inputNamaAkhir setelah mengedit
                kodeInput.setText("");
                stokInput.setText("");
                hargaInput.setText("");
                namaInput.setText("");
                jenisObatButtonGroup.clearSelection();

            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan diedit.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedRow = tableObat.getSelectedRow();
        if (selectedRow >= 0) {
            model = (DefaultTableModel) tableObat.getModel();
            model.removeRow(selectedRow);
            obatList.remove(selectedRow);

        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

