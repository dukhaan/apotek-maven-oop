    private void tableObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableObatMouseClicked
        int selectedRow = tableObat.getSelectedRow();
        int columnIndexKode = 0;
        int columnIndexNama = 1;
        int columnIndexHarga = 2;
        int columnIndexStok = 3;
        int columnIndexJenis = 4;
        
        if (selectedRow >= 0) {
            DefaultTableModel model = (DefaultTableModel) tableObat.getModel();
            
            String kodeObat = model.getValueAt(selectedRow, columnIndexKode).toString();
            String namaObat = model.getValueAt(selectedRow, columnIndexNama).toString();
            String hargaObat = model.getValueAt(selectedRow, columnIndexHarga).toString();
            String stokObat = model.getValueAt(selectedRow, columnIndexStok).toString();
            String jenisObat = model.getValueAt(selectedRow, columnIndexJenis).toString();

            kodeInput.setText(kodeObat);
            namaInput.setText(namaObat);
            hargaInput.setText(hargaObat);
            stokInput.setText(stokObat);
            
            opsiSirup.setSelected(false);
            opsiCair.setSelected(false);
            opsiKapsul.setSelected(false);
            opsiTablet.setSelected(false);
            
            if (jenisObat.equals("Sirup")) {
                opsiSirup.setSelected(true);
            } else if (jenisObat.equals("Cair")) {
                opsiCair.setSelected(true);
            } else if (jenisObat.equals("Kapsul")) {
                opsiKapsul.setSelected(true);
            } else if (jenisObat.equals("Tablet")) {
                opsiTablet.setSelected(true);
            }
        }
    }//GEN-LAST:event_tableObatMouseClicked


